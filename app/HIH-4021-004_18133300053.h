#define HIH_VOLTAGE_AT_75_3RH (3.094102f * DIVIDER_COEFFICIENT)
#define HIH_SLOPE ((HIH_VOLTAGE_AT_75_3RH - HIH_VOLTAGE_AT_0RH) / 75.3f)
